enum {
	HUD_UPLOAD_BAND_HEIGHT = 12,
	HUD_PALETTE_COUNT = RENDER_PALETTE_ART_8 + 1,
};

static uint8_t hud_upload_buffer[PVZ_MAX_HUD_WIDTH * HUD_UPLOAD_BAND_HEIGHT * 3];
static uint8_t hud_palette_rgb666[256][3];
static bool hud_palette_rgb666_initialized = false;

static int min_int(int a, int b) { return a < b ? a : b; }

static uint32_t frame_timer_now_us(void) { return __HAL_TIM_GET_COUNTER(&htim2); }

static uint32_t frame_timer_elapsed_us(uint32_t start_us, uint32_t end_us) { return end_us - start_us; }

static uint32_t fixed_dt_to_frame_us(float fixed_dt) {
	if (fixed_dt <= 0.0f) {
		fixed_dt = PVZ_DEFAULT_FIXED_DT;
	}

	return (uint32_t)(fixed_dt * 1000000.0f + 0.5f);
}

static float frame_dt_from_elapsed_us(uint32_t elapsed_us, float fallback_dt) {
	const float frame_dt = (float)elapsed_us / 1000000.0f;

	if (frame_dt <= 0.0f || frame_dt > (fallback_dt * 4.0f)) {
		return fallback_dt;
	}

	return frame_dt;
}

static void rgb565_to_rgb666_bytes(uint16_t color, uint8_t rgb[3]) {
	rgb[0] = (uint8_t)(((color >> 11) & 0x1Fu) << 3);
	rgb[1] = (uint8_t)(((color >> 5) & 0x3Fu) << 2);
	rgb[2] = (uint8_t)((color & 0x1Fu) << 3);
}

static void init_hud_palette_rgb666(void) {
	if (hud_palette_rgb666_initialized) {
		return;
	}

	for (int palette = 0; palette < HUD_PALETTE_COUNT; ++palette) {
		rgb565_to_rgb666_bytes(presentation_palette_to_rgb565((RenderPalette)palette), hud_palette_rgb666[palette]);
	}

	hud_palette_rgb666_initialized = true;
}

static void init_hub75_palette(void) {
	for (int palette = 0; palette < HUD_PALETTE_COUNT; ++palette) {
		hub75_set_palette_rgb565((uint8_t)palette, presentation_palette_to_rgb565((RenderPalette)palette));
	}
}

static void upload_to_hud(const RenderView *view) {
	if (view == NULL || view->hud_dirty_rects.count <= 0 || view->hud_width <= 0 || view->hud_height <= 0) {
		return;
	}

	init_hud_palette_rgb666();

	for (int rect_index = 0; rect_index < view->hud_dirty_rects.count; ++rect_index) {
		IntRect rect = view->hud_dirty_rects.rects[rect_index];
		if (rect.x < 0) {
			rect.w += rect.x;
			rect.x = 0;
		}
		if (rect.y < 0) {
			rect.h += rect.y;
			rect.y = 0;
		}
		if (rect.x + rect.w > view->hud_width) {
			rect.w = view->hud_width - rect.x;
		}
		if (rect.y + rect.h > view->hud_height) {
			rect.h = view->hud_height - rect.y;
		}
		if (rect.w <= 0 || rect.h <= 0) {
			continue;
		}

		for (int band_y = 0; band_y < rect.h; band_y += HUD_UPLOAD_BAND_HEIGHT) {
			const int band_height = min_int(HUD_UPLOAD_BAND_HEIGHT, rect.h - band_y);
			const uint8_t *src_row = &view->hud_pixels[(rect.y + band_y) * view->hud_width + rect.x];
			uint8_t *dst = hud_upload_buffer;

			for (int row = 0; row < band_height; ++row) {
				const uint8_t *src = src_row + row * view->hud_width;
				for (int col = 0; col < rect.w; ++col) {
					const uint8_t *rgb = hud_palette_rgb666[src[col]];
					*dst++ = rgb[0];
					*dst++ = rgb[1];
					*dst++ = rgb[2];
				}
			}

			ILI9488_DrawImage((uint16_t)rect.x, (uint16_t)(rect.y + band_y), (uint16_t)rect.w, (uint16_t)band_height,
							  hud_upload_buffer);
		}
	}
}

